if ( ! function_exists( 'quest_is_catalog_mode' ) ) {
	function quest_is_catalog_mode(): bool {
		if ( ! function_exists( 'get_field' ) ) {
			return true;
		}
		$val = get_field( 'catalog_mode', 'option' );
		return $val === null ? true : (bool) $val;
	}
}

function quest_is_woo_page(): bool {
	if ( ! function_exists( 'is_woocommerce' ) ) {
		return false;
	}
	return is_woocommerce() || is_cart() || is_checkout() || is_account_page();
}

/**
 * Output an ACF theme option with wp_kses_post escaping.
 */
function quest_option( string $key, string $default = '', bool $echo = true ): string {
	$val = function_exists( 'get_field' ) ? ( get_field( $key, 'option' ) ?: $default ) : $default;
	$val = wp_kses_post( $val );
	if ( $echo ) {
		echo $val;
	}
	return $val;
}

/**
 * Output a raw ACF option value escaped as plain text.
 */
function quest_option_text( string $key, string $default = '', bool $echo = true ): string {
	$val = function_exists( 'get_field' ) ? ( get_field( $key, 'option' ) ?: $default ) : $default;
	$val = esc_html( $val );
	if ( $echo ) {
		echo $val;
	}
	return $val;
}

if ( ! function_exists( 'quest_account_url' ) ) {
	function quest_account_url(): string {
		if ( function_exists( 'wc_get_account_endpoint_url' ) ) {
			return wc_get_account_endpoint_url( 'dashboard' );
		}
		return home_url( '/my-account/' );
	}
}

if ( ! function_exists( 'quest_shop_url' ) ) {
	function quest_shop_url(): string {
		if ( function_exists( 'wc_get_page_permalink' ) ) {
			return wc_get_page_permalink( 'shop' );
		}
		return home_url( '/shop/' );
	}
}

